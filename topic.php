    <h2> What are the advantages and disadvantages of the MP3 </h2>
    <section>
      <h3> Advantages </h3>
      <ol>
        <li> Without patents: The cost of distributing music via streaming is low, allowing new artists to distribute
          their compositions because they don't have to pay for a medium like a CD to access their music. This is also
          enabled for streaming audio books, podcasts and sound effects </li>
        <li> Cost: The MP3 format is currently a free format, it does not require paying licenses or royalties for
          patents </li>
        <li> Practicality: MP3 is an audio format that can be easily transmitted over the network </li>
      </ol>
      <h3> Disadvantages </h3>
      <ol>
        <li> Loss of Sound Quality: MP3 files are considered a "lossy" digital audio format type for a reason when MP3
          files are compressed, the original recording quality is sacrificed to make the file size smaller.</li>
      </ol>
    </section>
    <h2> What is the current and future state of MP3 </h2>
    <p> The mp3 format is over 30 years old, so the age of MP3 is officially over, which means that newly released
      devices will stop supporting the mp3 file.</p>

