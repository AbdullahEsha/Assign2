  <h2> What is an MP3?</h2>
  <P>An mp3 file is an audio file type created using compression technology to reduce file size. It is called a
    "lossy" format because the compression process is irreversible. An average compressed mp3 file is about 3 MB.
  </p>
  <h2> What is the File MP3 used For </h2>
  <p> The MP3 file format is used to store digital audio data. </p>
